// 初始化地址选择器
function initAddressSelector(options) {
    var selProvince = document.getElementById('selProvince');
    var selCity = document.getElementById('selCity');
    var selDistrict = document.getElementById('selDistrict');
    var selStreet = document.getElementById('selStreet');

    if (!selProvince) {
        console.log('地址选择器初始化失败: selProvince 不存在');
        return;
    }

    // 调试：显示传入的选项
    console.log('地址选择器初始化, options:', options);

    // 填充省份
    selProvince.innerHTML = '<option value="">请选择省</option>';
    AreaData.provinces.forEach(function(province) {
        selProvince.innerHTML += '<option value="' + province.name + '">' + province.name + '</option>';
    });

    // 省份选择事件
    selProvince.addEventListener('change', function() {
        var provinceName = this.value;
        var cities = AreaData.cities[provinceName] || [];

        selCity.innerHTML = '<option value="">请选择市</option>';
        selDistrict.innerHTML = '<option value="">请选择区/县</option>';
        selStreet.innerHTML = '<option value="">请选择街道</option>';

        cities.forEach(function(city) {
            selCity.innerHTML += '<option value="' + city + '">' + city + '</option>';
        });
    });

    // 市选择事件
    selCity.addEventListener('change', function() {
        var cityName = this.value;
        var districts = AreaData.districts[cityName] || [];

        selDistrict.innerHTML = '<option value="">请选择区/县</option>';
        selStreet.innerHTML = '<option value="">请选择街道</option>';

        districts.forEach(function(district) {
            selDistrict.innerHTML += '<option value="' + district + '">' + district + '</option>';
        });
    });

    // 区/县选择事件
    selDistrict.addEventListener('change', function() {
        var districtName = this.value;
        var streets = AreaData.streets[districtName] || [];

        selStreet.innerHTML = '<option value="">请选择街道</option>';

        if (streets.length > 0) {
            streets.forEach(function(street) {
                selStreet.innerHTML += '<option value="' + street + '">' + street + '</option>';
            });
        } else {
            // 如果没有街道数据，提供通用选项
            selStreet.innerHTML += '<option value="其他街道">其他街道</option>';
        }
    });

    // 如果有初始值，触发选择
    if (options && options.province && options.province.length > 0) {
        console.log('开始设置省份:', options.province);
        
        // 设置省份
        for (var i = 0; i < selProvince.options.length; i++) {
            if (selProvince.options[i].value === options.province) {
                selProvince.selectedIndex = i;
                console.log('省份设置成功, index:', i);
                selProvince.dispatchEvent(new Event('change'));
                break;
            }
        }

        // 延迟设置城市
        setTimeout(function() {
            if (options.city && options.city.length > 0) {
                console.log('设置城市:', options.city);
                for (var i = 0; i < selCity.options.length; i++) {
                    if (selCity.options[i].value === options.city) {
                        selCity.selectedIndex = i;
                        console.log('城市设置成功');
                        selCity.dispatchEvent(new Event('change'));
                        break;
                    }
                }
            }

            // 延迟设置区县
            setTimeout(function() {
                if (options.district && options.district.length > 0) {
                    console.log('设置区县:', options.district);
                    for (var i = 0; i < selDistrict.options.length; i++) {
                        if (selDistrict.options[i].value === options.district) {
                            selDistrict.selectedIndex = i;
                            console.log('区县设置成功');
                            selDistrict.dispatchEvent(new Event('change'));
                            break;
                        }
                    }
                }

                // 延迟设置街道
                setTimeout(function() {
                    if (options.street && options.street.length > 0) {
                        console.log('设置街道:', options.street);
                        for (var i = 0; i < selStreet.options.length; i++) {
                            if (selStreet.options[i].value === options.street) {
                                selStreet.selectedIndex = i;
                                console.log('街道设置成功');
                                break;
                            }
                        }
                    }
                }, 100);
            }, 100);
        }, 100);
    } else {
        console.log('没有初始地址数据或省份为空');
    }
}